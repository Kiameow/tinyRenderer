struct TGAColor {
	union {
		struct {
			unsigned char b, g, r, a;
		};
		unsigned char raw[4];
		unsigned int val;
	};
	int bytespp;

	TGAColor() : val(0), bytespp(1) {
	}

	TGAColor(unsigned char R, unsigned char G, unsigned char B, unsigned char A) : b(B), g(G), r(R), a(A), bytespp(4) {
	}

	TGAColor(int v, int bpp) : val(v), bytespp(bpp) {
	}

	TGAColor(const TGAColor &c) : val(c.val), bytespp(c.bytespp) {
	}

	TGAColor(const unsigned char *p, int bpp) : val(0), bytespp(bpp) {
		for (int i=0; i<bpp; i++) {
			raw[i] = p[i];
		}
	}

	TGAColor & operator =(const TGAColor &c) {
		if (this != &c) {
			bytespp = c.bytespp;
			val = c.val;
		}
		return *this;
	}

	TGAColor operator *(float intensity) {
		if (intensity < 0) 
			throw std::underflow_error("TGAColor cannot be negative");

		TGAColor c(
			std::min(static_cast<int>(this->r * intensity), 255), 
			std::min(static_cast<int>(this->g * intensity), 255), 
			std::min(static_cast<int>(this->b * intensity), 255), 
			this->a
		);

		return c;
	}

	unsigned char& operator[](size_t i) {
		switch(i) {
			case 0:
				return r;
			case 1:
				return g;
			case 2:
				return b;
			case 3:
				return a;
			default:
				throw "beyond the TGAColor boundary";
		}
	}
};

